  //while dan do while
  var angka = 2;
  
  console.log("while:");
  while (angka <= 5) {
    console.log(angka);
    angka++;
  }
  
  console.log("Do While:");
  do {
    console.log(angka);
    angka--;
  } while (angka >= 1);
  